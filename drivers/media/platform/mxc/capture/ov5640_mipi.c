static void ov5640_standby(bool enable)
{
	pr_debug("%s: %d\n", __func__, enable);
	if (enable) {
		gpio_set_value(rst_gpio, 0);
		gpio_set_value(pwn_gpio, 1);
	} else
		gpio_set_value(pwn_gpio, 0);

	msleep(2);
}

static void ov5640_reset(void)
{
	pr_debug("%s\n", __func__);
	ov5640_standby(0);
	msleep(1);
	/* camera reset */
	gpio_set_value(rst_gpio, 1);
	msleep(20);
}

static int ov5640_set_power(bool on)
{
	pr_debug("%s: %d\n", __func__, on);

	if (ov5640_power_state == on)
		return 0;

	if (on) {
		if (io_regulator && !regulator_is_enabled(io_regulator))
			if (regulator_enable(io_regulator) != 0)
				return -EIO;
		if (analog_regulator && !regulator_is_enabled(analog_regulator))
			if (regulator_enable(analog_regulator) != 0)
				return -EIO;
		if (gpo_regulator && !regulator_is_enabled(gpo_regulator))
			if (regulator_enable(gpo_regulator) != 0)
				return -EIO;
		/* Make sure power on */
		msleep(5);
		clk_prepare_enable(ov5640_data.sensor_clk);
		ov5640_reset();
	} else {
		cancel_delayed_work_sync(&autofocus_work);
		ov5640_autofocus_loaded = false;

		ov5640_standby(1);
		clk_disable_unprepare(ov5640_data.sensor_clk);

		if (af_regulator && regulator_is_enabled(af_regulator))
			regulator_disable(af_regulator);
		if (gpo_regulator && regulator_is_enabled(gpo_regulator))
			regulator_disable(gpo_regulator);
		if (io_regulator && regulator_is_enabled(io_regulator))
			regulator_disable(io_regulator);
		if (analog_regulator && regulator_is_enabled(analog_regulator))
			regulator_disable(analog_regulator);
	}

	ov5640_power_state = on;

	return 0;
}

