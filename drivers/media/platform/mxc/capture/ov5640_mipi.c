static int ov5640_download_autofocus(void)
{
	u8  r;
	int sval;
       
	sval = ov5640_read_reg(0x3000, &r);
	if (0 > sval) {
		pr_err("%s(mipi):Error reading control reg\n",
		       __func__);
		return sval;
	}
	r |= 0x20;
	ov5640_write_reg(0x3000,r);

	sval = ov5640_write_block(ov5640_af_firmware,
				  ARRAY_SIZE(ov5640_af_firmware));
	if (sval < 0) {
		pr_err("%s(mipi): Error downloading firmware\n",
		       __func__);
		return sval;
	}

	sval = ov5640_download_firmware(
		ov5640_af_firmware_ctl,
		ARRAY_SIZE(ov5640_af_firmware_ctl));
	if (0 > sval) {
		pr_err("%s(mipi): Error running firmware\n",
		       __func__);
	} else
		pr_info("%s(mipi): Downloaded firmware successfully: %d\n",
		       __func__,sval);
	r &= ~0x20;
	ov5640_write_reg(0x3000,r);

	/* VCM control4 */
	ov5640_write_reg(0x3606,0x07);

	return sval;
}

static void ov5640_auto_focus_prepare(void)
{
	mutex_lock(&autofocus_lock);

	if (!ov5640_autofocus_loaded) {
		if (af_regulator && !regulator_is_enabled(af_regulator)) {
			if (regulator_enable(af_regulator) != 0)
				pr_err("AFVDD error\n");
		}

		ov5640_download_autofocus();
		ov5640_autofocus_loaded = true;
	}

	mutex_unlock(&autofocus_lock);
}

static int ov5640_auto_focus_start(void)
{
	ov5640_write_reg(CMD_ACK, CMD_ACK_CODE);
	ov5640_write_reg(CMD_MAIN, CMD_AF_TRIGGER);

	return 0;
}

static void autofocus_worker(struct work_struct *work)
{
	ov5640_auto_focus_prepare();
	ov5640_auto_focus_start();
}

static int ov5640_auto_focus_stop(void)
{
	ov5640_write_reg(CMD_MAIN, CMD_AF_PAUSE);
	return 0;
}

static int ov5640_auto_focus_status(void)
{
	int ret;
	u8 val;

	ret = ov5640_read_reg(FW_STATUS, &val);
	if (ret < 0)
		return V4L2_AUTO_FOCUS_STATUS_FAILED;

	pr_debug("%s: FW_STATUS:0x%.2x\n", __func__, val);

	switch (val) {
	case FW_S_FIRMWARE:
	case FW_S_STARTUP:
	case FW_S_FOCUSING:
		return V4L2_AUTO_FOCUS_STATUS_BUSY;
	case FW_S_IDLE:
		return V4L2_AUTO_FOCUS_STATUS_IDLE;
	case FW_S_FOCUSED:
		return V4L2_AUTO_FOCUS_STATUS_REACHED;
	}

	return V4L2_AUTO_FOCUS_STATUS_FAILED;
}

static int ov5640_strobe_output_enable(void)
{
	u8 val;

	pr_debug("ov5640_strobe_output_enable\n");

	ov5640_read_reg(PAD_OE0, &val);
	ov5640_write_reg(PAD_OE0, val | PAD_OE0_STROBE_EN);
	ov5640_write_reg(FREX_MODE, FREX_MODE_ROLLING_STROBE);

	return 0;
}

static int ov5640_flash_strobe(int mode)
{
	int value = STROBE_CTRL_MODE_LED3;

	pr_debug("ov5640_flash_strobe %d\n", mode);

	if (mode == 1 || (mode == 2 && underexposed))
		value |= STROBE_CTRL_REQ;

	ov5640_write_reg(STROBE_CTRL, value);
	return 0;
}

static int ov5640_auto_focus_set_pos(int pos)
{
	int ret;
	u8 temp;

	cancel_delayed_work_sync(&autofocus_work);
	ov5640_auto_focus_prepare();

	pr_debug("Stepping to position: %d\n", pos);
	if (pos < 0 || pos > 255)
		return -EINVAL;

	ret = ov5640_read_reg(0x3602, &temp);
	if (ret < 0) {
		pr_err("ov5640_read_reg(3602): %d\n", ret);
		return ret;
	}

	temp &= 0x0f;
	temp |= (pos & 0x7) << 5;	/* lower 3 bits */

	ov5640_write_reg(0x3602, temp);
	ov5640_write_reg(0x3603, pos >> 3);

	return 0;
}

static int ioctl_send_command(struct v4l2_int_device *s,
		struct v4l2_send_command_control *vc)
{
	int ret = -1;

	switch (vc->id) {
		case 105:
			ret = ov5640_auto_focus_set_pos(vc->value0);
			break;
		default:
			pr_err("%s:Unknown ctrl 0x%x\n", __func__, vc->id);
			break;
	}

	return ret;
}

static int ov5640_init_mode(enum ov5640_frame_rate frame_rate,
			    enum ov5640_mode mode, enum ov5640_mode orig_mode)
{
	struct reg_value *pModeSetting = NULL;
	s32 ArySize = 0;
	int retval = 0;
	void *mipi_csi2_info;
	u32 mipi_reg, msec_wait4stable = 0;
	enum ov5640_downsize_mode dn_mode, orig_dn_mode;

	if ((mode > ov5640_mode_MAX || mode < ov5640_mode_MIN)
		&& (mode != ov5640_mode_INIT)) {
		pr_err("Wrong ov5640 mode detected!\n");
		return -1;
	}

	mipi_csi2_info = mipi_csi2_get_info();

	/* initial mipi dphy */
	if (!mipi_csi2_info) {
		printk(KERN_ERR "%s() in %s: Fail to get mipi_csi2_info!\n",
		       __func__, __FILE__);
		return -1;
	}

	if (!mipi_csi2_get_status(mipi_csi2_info))
		mipi_csi2_enable(mipi_csi2_info);

	if (!mipi_csi2_get_status(mipi_csi2_info)) {
		pr_err("Can not enable mipi csi2 driver!\n");
		return -1;
	}

	mipi_csi2_set_lanes(mipi_csi2_info);

	/*Only reset MIPI CSI2 HW at sensor initialize*/
	if (mode == ov5640_mode_INIT)
		mipi_csi2_reset(mipi_csi2_info);

	if (ov5640_data.pix.pixelformat == V4L2_PIX_FMT_UYVY)
		mipi_csi2_set_datatype(mipi_csi2_info, MIPI_DT_YUV422);
	else if (ov5640_data.pix.pixelformat == V4L2_PIX_FMT_RGB565)
		mipi_csi2_set_datatype(mipi_csi2_info, MIPI_DT_RGB565);
	else
		pr_err("currently this sensor format can not be supported!\n");

	dn_mode = ov5640_mode_info_data[frame_rate][mode].dn_mode;
	orig_dn_mode = ov5640_mode_info_data[frame_rate][orig_mode].dn_mode;
	if (mode == ov5640_mode_INIT) {
		pModeSetting = ov5640_init_setting_30fps_VGA;
		ArySize = ARRAY_SIZE(ov5640_init_setting_30fps_VGA);

		ov5640_data.pix.width = 640;
		ov5640_data.pix.height = 480;
		retval = ov5640_download_firmware(pModeSetting, ArySize);
		if (retval < 0)
			goto err;

		pModeSetting = ov5640_setting_30fps_VGA_640_480;
		ArySize = ARRAY_SIZE(ov5640_setting_30fps_VGA_640_480);
		retval = ov5640_download_firmware(pModeSetting, ArySize);

		if (retval == 0)
			schedule_delayed_work(&autofocus_work, HZ);

	} else if ((dn_mode == SUBSAMPLING && orig_dn_mode == SCALING) ||
			(dn_mode == SCALING && orig_dn_mode == SUBSAMPLING)) {
		/* change between subsampling and scaling
		 * go through exposure calucation */
		retval = ov5640_change_mode_exposure_calc(frame_rate, mode);
	} else {
		/* change inside subsampling or scaling
		 * download firmware directly */
		retval = ov5640_change_mode_direct(frame_rate, mode);
	}

	if (retval < 0)
		goto err;

	OV5640_set_AE_target(AE_Target);
	OV5640_get_light_freq();
	OV5640_set_bandingfilter();
	ov5640_set_virtual_channel(ov5640_data.csi);
	ov5640_strobe_output_enable();

	/* add delay to wait for sensor stable */
	if (mode == ov5640_mode_QSXGA_2592_1944) {
		/* dump the first two frames: 1/7.5*2
		 * the frame rate of QSXGA is 7.5fps */
		msec_wait4stable = 267;
	} else if (frame_rate == ov5640_15_fps) {
		/* dump the first nine frames: 1/15*9 */
		msec_wait4stable = 600;
	} else if (frame_rate == ov5640_30_fps) {
		/* dump the first nine frames: 1/30*9 */
		msec_wait4stable = 300;
	}
	msleep(msec_wait4stable);

	if (mipi_csi2_info) {
		unsigned int i;

		i = 0;

		/* wait for mipi sensor ready */
		mipi_reg = mipi_csi2_dphy_status(mipi_csi2_info);
		while ((mipi_reg == 0x200) && (i < 10)) {
			mipi_reg = mipi_csi2_dphy_status(mipi_csi2_info);
			i++;
			msleep(10);
		}

		if (i >= 10) {
			pr_err("mipi csi2 can not receive sensor clk!\n");
			return -1;
		}

		i = 0;

		/* wait for mipi stable */
		mipi_reg = mipi_csi2_get_error1(mipi_csi2_info);
		while ((mipi_reg != 0x0) && (i < 10)) {
			mipi_reg = mipi_csi2_get_error1(mipi_csi2_info);
			i++;
			msleep(10);
		}

		if (i >= 10) {
			pr_err("mipi csi2 can not reveive data correctly!\n");
			return -1;
		}
	}
err:
	return retval;
}

